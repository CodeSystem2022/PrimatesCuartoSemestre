# Profundizando en los sistemas de numeracion

# Sistema decimal
a = 10
print(f'a decimal: {a}')

# Sistema binario
a = 0b1010
print(f'a binario: {a}')

# Sistema octal
a = 0o12
print(f'a octal: {a}')

# Ssitema hexadecimal
a = 0xA
print(f'a hexadecimal: {a}')

# Base decimal
a = int('23', 10)
print(f'a: {a}')

# Base Binario
a = int('1010', 2)
print(f'a binario: {a}')

#Base Octal
a = int('12')

